class Char {
	public static void main(String args[]) {
		char c1 = 'D';
		char c2 = 122;
		char c3 = '\u0122';
		char c4 = '\u007A';
		char c5 = (char)-122;
		int i5 = c5;
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		System.out.println(c4);
		System.out.println(c5);
		System.out.println(i5);
	}
}